## Global Scope
Setiap project yang menggunakan framework ini tidak lepas dari pattern kinerja yang terbagi atas beberapa bagian atau scope. 
Setiap scope memiliki spesialisasi tugas dan tanggung jawab masing-masing. Secara garis besar, berikut bagian scope yang terlibat:
- System Analyst
- Frontend Developer
- Backend Web Developer
- Backend Database Engineer
- System Administrator


## Scope: System Analyst
Merupakan scope yang memiliki tugas dan tanggung jawab dalam menganalisa kebutuhan sistem. 
Tugas utama yakni dimulai dengan mendefinisikan masalah hingga memberikan solusi rancangan teknis.
Output dari scope ini adalah Diagram Alir, Data Flow Diagram, SOP Proses Bisnis, Dirty UI Design, Use Case Diagram, dan Mapping Kebutuhan Data.
Dalam pelaksanaannya, scope ini juga harus melakukan re-checking atas hasil kinerja tim developer apakah implementasi development sudah sesuai perancangan yang dibuat.

## Scope: Frontend Developer
Merupakan scope yang memiliki tugas dan tanggung jawab berupa mengimplementasikan UI(User Interface) dari program berdasarkan dirty UI yang dihasilkan melalui tahap perencanaan oleh para System Analyst.
UI ini biasa berupa tampilan Form, Tabel, Dashboard, Profil, dan macam-macam. Seorang frontend developer harus benar-benar memahami dirty UI yang diberikan oleh System Analyst sebagai acuan dalam developing.
Selain mengimplementasikan UI, frontend developer wajib melakukan komunikasi data dengan backend developer dalam pelaksanaannya.


## Scope: Backend Web Developer
Merupakan scope yang memiliki tugas dan tanggung jawab berupa mengimplementasikan Rule atau Flow dari jalannya sistem yang dirancang.
Scope ini secara singkat mengakomodir semisal: User Authentication & Authorization, User Role, User Priviledges, Data Validation, Data Integration, Data Casting, Database Connection, dan lainnya.

