## Konsep
Konsep yang digunakan dalam membangun sistem adalah prinsip Software Development Life Cycle atau [SDLC](https://www.tutorialspoint.com/sdlc/sdlc_overview.htm).
Cycle putaran looping SDLC sangat ampuh dalam membangun sebuah aplikasi apapun di dunia IT.<br>
